function calculateAverages() {
  /* Calculates totals first, then missed days, then averages */
  AP = []
  AF = []
  AM = []
  AT = []
  AA = []
  TP = []
  TF = []
  TM = []
  TT = []
  TA = []
  MD = []
  MAD = []
  for (i = 0; i < mainArray.Name.length; i++) {
    LP = 0
    LF = 0
    LM = 0
    LT = 0
    LA = 0
    LMD = 0
    LMAD = 0
    for (x = 0; x < Data.length; x++) {
      Scored = false
      for (y = 0; y < Data[x].Scorer.length; y++) {
        if (Data[x].Scorer[y] == mainArray.Name[i]) {
          Scored = true
          if (Data[x].Type[y] == "Finish") {
            LF += 1
          } else if (Data[x].Type[y] == "Midrange") {
            LM += 1
          } else {
            LT += 1
          }
        }
        if (Data[x].Assister[y] == mainArray.Name[i]) {
          LA += 1
        }
      }
      if (Scored == false) {
        if (Missed[x].Missed[i] == 1) {
          LMD += 1
          if (x > 7) {
            LMAD += 1
          }
        }
      }
      LP = LF + LM + LT + LT
    }
    TP.push(LP)
    TF.push(LF)
    TM.push(LM)
    TT.push(LT)
    TA.push(LA)
    MD.push(LMD)
    MAD.push(LMAD)
  }

  for (i = 0; i < mainArray.Name.length; i++) {
    if ((Data.length - MD[i]) != 0) {
      AP.push((Math.round(100 * (TP[i] / (Data.length - MD[i])))) / 100)
      AF.push((Math.round(100 * (TF[i] / (Data.length - MD[i])))) / 100)
      AM.push((Math.round(100 * (TM[i] / (Data.length - MD[i])))) / 100)
      AT.push((Math.round(100 * (TT[i] / (Data.length - MD[i])))) / 100)
    } else {
      AP.push(0)
      AF.push(0)
      AM.push(0)
      AT.push(0)
    }
    if ((Data.length - (MAD[i] + 7)) != 0) {
      AA.push((Math.round(100 * (TA[i] / (Data.length - (MAD[i] + 7))))) / 100)
    } else {
      AA.push(0)
    }
    
  }

  mainArray.PPG = AP
  mainArray.TP = TP
  mainArray.FPG = AF
  mainArray.TF = TF
  mainArray.MPG = AM
  mainArray.TM = TM
  mainArray.TPG = AT
  mainArray.TT = TT
  mainArray.APG = AA
  mainArray.TA = TA
  mainArray.Missed = MD

}


